uint8_t DHT22_Start (void)
{
	GPIO_InitTypeDef GPIO_InitStruct = {0};
	uint8_t Response = 0;
	/*Configure GPIO pin : PC10 */
	GPIO_InitStruct.Pin = GPIO_PIN_9;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_9, GPIO_PIN_RESET);
	microDelay(1200);

	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_9, GPIO_PIN_SET);
	microDelay(30);

	GPIO_InitStruct.Pin = GPIO_PIN_9;
	GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
	GPIO_InitStruct.Pull = GPIO_PULLUP;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

	microDelay (40);
	if (!(HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_9)))
	{
		microDelay (80);
		if ((HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_9))) Response = 1;
	}

	pMillis = HAL_GetTick();
	cMillis = HAL_GetTick();
	while ((HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_9)) && pMillis + 2 > cMillis)
	{
	cMillis = HAL_GetTick();
	}

	return Response;
}

uint8_t DHT22_Read (void) //Read 1 byte
{
    uint8_t i = 0;
    for (uint8_t j = 0; j < 8; j++)
    {
//         等待低→高 transition
        while(!HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_9));

        // 開始計時
        __HAL_TIM_SET_COUNTER(&htim1, 0);
        while(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_9)); // 等到 high 結束
        uint32_t pulse = __HAL_TIM_GET_COUNTER(&htim1); // 高電平時間(us)

        if(pulse > 50) // 高電平 > 50us，判斷為 1
            i |= (1 << (7-j));
        else
            i &= ~(1 << (7-j));
    }
    return i;
}
